var card = document.getElementById('card');

card.addEventListener('dblclick', function() {
	if (!this.classList.contains('on')) {
		this.classList.remove('off');
		this.classList.add('on');
	} else {
		this.classList.remove('on');
		this.classList.add('off');
	}
});
